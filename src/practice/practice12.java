package practice;

public class practice12 {
	public static void main(String[] args) {
		//break
		for (int i = 0; i < 10; i++ ) {
			if (i == 5) {
				break;
			}
			
			System.out.println(i);
			
		}
		
		//continue
		for (int i = 0; i < 10; i++) {
			if (i == 5) {
				continue;
				
			}
			
			System.out.println(i);
		}
			
	}
	

}
