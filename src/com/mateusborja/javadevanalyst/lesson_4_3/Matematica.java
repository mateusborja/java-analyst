package com.mateusborja.javadevanalyst.lesson_4_3;

public class Matematica {

	int somar(int x, int y) {
		System.out.println("int somar(int x, int y)");
		return x + y;
	}

	int somar(int x, int y, int z) {
		System.out.println("int somar(int x, int y, int z)");
		return x + y + z;
	}

	double somar(double x, double y) {
		System.out.println("double somar(double x, double y)");
		return x + y;
	}

	double somar(double x, double y, double z) {
		System.out.println("double somar(double x, double y, double z)");
		return x + y + z;
	}

}
